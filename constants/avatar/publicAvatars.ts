export const AVATAR_ORIENTATIONS = ['portrait', 'landscape'] as const;

export const AVATAR_TAGS = [
  'AVATAR_IV',
  'EXPRESSIVE', 
  'AI_GENERATED',
  'NEW'
] as const;

