export function getAvatarStats(avatars: PublicAvatar[] = PUBLIC_AVATARS) {
  return {
    total: avatars.length,
    byGender: {
      male: avatars.filter(a => a.gender === 'male').length,
      female: avatars.filter(a => a.gender === 'female').length,
      unknown: avatars.filter(a => a.gender === 'unknown').length,
    },
    byType: AVATAR_TYPES.reduce((acc, type) => ({
      ...acc,
      [type]: avatars.filter(a => a.avatarType === type).length
    }), {} as Record<string, number>),
    byOrientation: {
      portrait: avatars.filter(a => a.orientation === 'portrait').length,
      landscape: avatars.filter(a => a.orientation === 'landscape').length,
    },
    premium: avatars.filter(a => a.isPremium).length,
    byQuality: {
      high: avatars.filter(a => a.quality === 'high').length,
      medium: avatars.filter(a => a.quality === 'medium').length,
      low: avatars.filter(a => a.quality === 'low').length,
    }
  };
}

