// ============================================================================
// FILTER FUNCTIONS
// ============================================================================

export function filterAvatarsByGender(avatars: PublicAvatar[], gender: string): PublicAvatar[] {
  if (!gender || gender === 'all') return avatars;
  return avatars.filter(avatar => avatar.gender === gender);
}

export function filterAvatarsByType(avatars: PublicAvatar[], type: string): PublicAvatar[] {
  if (!type || type === 'all') return avatars;
  return avatars.filter(avatar => avatar.avatarType === type);
}

export function filterAvatarsByOrientation(avatars: PublicAvatar[], orientation: string): PublicAvatar[] {
  if (!orientation || orientation === 'all') return avatars;
  return avatars.filter(avatar => avatar.orientation === orientation);
}

export function filterAvatarsByTag(avatars: PublicAvatar[], tag: string): PublicAvatar[] {
  if (!tag || tag === 'all') return avatars;
  return avatars.filter(avatar => avatar.tags.includes(tag));
}

export function filterAvatarsByPremium(avatars: PublicAvatar[], isPremium?: boolean): PublicAvatar[] {
  if (isPremium === undefined) return avatars;
  return avatars.filter(avatar => avatar.isPremium === isPremium);
}

export function filterAvatarsByQuality(avatars: PublicAvatar[], quality: string): PublicAvatar[] {
  if (!quality || quality === 'all') return avatars;
  return avatars.filter(avatar => avatar.quality === quality);
}

export function searchAvatarsByName(avatars: PublicAvatar[], searchTerm: string): PublicAvatar[] {
  if (!searchTerm) return avatars;
  const term = searchTerm.toLowerCase();
  return avatars.filter(avatar => 
    avatar.name.toLowerCase().includes(term)
  );
}

export function sortAvatarsByScore(avatars: PublicAvatar[], ascending = false): PublicAvatar[] {
  return [...avatars].sort((a, b) => 
    ascending ? a.debugScore - b.debugScore : b.debugScore - a.debugScore
  );
}

export function sortAvatarsByName(avatars: PublicAvatar[], ascending = true): PublicAvatar[] {
  return [...avatars].sort((a, b) => 
    ascending ? a.name.localeCompare(b.name) : b.name.localeCompare(a.name)
  );
}

export function sortAvatarsByCreatedAt(avatars: PublicAvatar[], ascending = false): PublicAvatar[] {
  return [...avatars].sort((a, b) => 
    ascending ? a.createdAt - b.createdAt : b.createdAt - a.createdAt
  );
}

