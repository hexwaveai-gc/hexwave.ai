// COMBINED FILTER FUNCTION
// ============================================================================

export interface AvatarFilters {
  gender?: string;
  avatarType?: string;
  orientation?: string;
  tag?: string;
  isPremium?: boolean;
  quality?: string;
  searchTerm?: string;
  sortBy?: 'name' | 'score' | 'createdAt';
  sortOrder?: 'asc' | 'desc';
  limit?: number;
}

export function filterAvatars(filters: AvatarFilters = {}): PublicAvatar[] {
  let filtered = [...PUBLIC_AVATARS];

  // Apply filters
  if (filters.gender) {
    filtered = filterAvatarsByGender(filtered, filters.gender);
  }
  
  if (filters.avatarType) {
    filtered = filterAvatarsByType(filtered, filters.avatarType);
  }
  
  if (filters.orientation) {
    filtered = filterAvatarsByOrientation(filtered, filters.orientation);
  }
  
  if (filters.tag) {
    filtered = filterAvatarsByTag(filtered, filters.tag);
  }
  
  if (filters.isPremium !== undefined) {
    filtered = filterAvatarsByPremium(filtered, filters.isPremium);
  }
  
  if (filters.quality) {
    filtered = filterAvatarsByQuality(filtered, filters.quality);
  }
  
  if (filters.searchTerm) {
    filtered = searchAvatarsByName(filtered, filters.searchTerm);
  }

  // Apply sorting
  if (filters.sortBy) {
    const ascending = filters.sortOrder === 'asc';
    
    switch (filters.sortBy) {
      case 'name':
        filtered = sortAvatarsByName(filtered, ascending);
        break;
      case 'score':
        filtered = sortAvatarsByScore(filtered, ascending);
        break;
      case 'createdAt':
        filtered = sortAvatarsByCreatedAt(filtered, ascending);
        break;
    }
  }

  // Apply limit
  if (filters.limit && filters.limit > 0) {
    filtered = filtered.slice(0, filters.limit);
  }

  return filtered;
}

